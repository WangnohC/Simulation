/*************************************************************************************
* 描述：getProbability()函数用于计算卫星对目标的探测概率，输入为最小信噪比阈值snr、实*                                                                             *
*       际信噪比SNR和虚警概率pj，输出为卫星对导弹的探测概率P。                       *
*************************************************************************************/
double Probability::getProbability(double SNR, double pj)                          //计算卫星对目标的探测概率
{
	double a = -2 * log(pj);
	double b = sqrt(a);
	double c = (5 * SNR / 15 - b);
	double P = 0.5 * erfc(- c / sqrt(2));         //利用标准正态分布计算探测概率
	
	return P;
}
